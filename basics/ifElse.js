var actualMarks = 10


if (actualMarks == 10) {
    console.log('Amazing!')
} else if (actualMarks == 5) {
    console.log('You did well')
} else if (actualMarks == 3) {
    console.log('Make it up next time')
} else if (actualMarks == 0) {
    console.log('BAD BAD BAD')
} else if (actualMarks < 10 && actualMarks > 5) { // Range check
    console.log('6 - 9')
} 
else {
    console.log('I am inside of ELSE block')
}


